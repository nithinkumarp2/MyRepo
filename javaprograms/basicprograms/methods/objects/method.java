class method 
{
	public void m1()
	{
		System.out.println("a");
	}
	public static void m1()
	{
		System.out.println();
	}
	public static void main(String[] args) 
	{  
		m1();

		System.out.println("Hello World!");
	}
}
//it is not method overloading 
//METHOD OVERLOADING MEANS : Method name should be same but differ in arguments
