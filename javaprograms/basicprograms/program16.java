class program16       //pre & post increment(++a & a++) 
{
	public static void main(String[] args) 
	{
		int a=37;
		int b=55;
		System.out.println(++a + ++b);
		System.out.println(a++ + ++b);
		System.out.println(++a + b++);
		System.out.println(a++ + b++);
		System.out.println(a);
		System.out.println(b);
	}
}
